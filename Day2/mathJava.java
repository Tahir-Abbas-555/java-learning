public class mathJava {
    public static void main(String[] args) {
        System.out.println("Minimum value: " + (Math.min(0, 1)));
        System.out.println("Maximum value: " + (Math.max(0, 1)));
        System.out.println("Square root: " + (Math.sqrt(64)));
        System.out.println("Power: " + (Math.pow(3, 2)));
        System.out.println("Absolute value: " + (Math.abs(-5000)));
        System.out.println("Rounded value: " + (Math.round(4.6)));
        System.out.println("Ceiling value: " + (Math.ceil(4.1))); 
        System.out.println("Floor value: " + (Math.floor(4.9)));
        System.out.println("Random value: " + ((int) (Math.random() * 101)));
    }
}